class Program20
{
	public static void main(String args[])
	{
		for(int i=1;i<=5;i++)                  
		{
			for(int j=1;j<=5;j++)          
			{
				for(int k=1;k<=5;k++)  
					{
					for(int m=1;m<=5;m++)
						{
						System.out.print("*");
						}
						System.out.print("  ");
					}
					System.out.println();
			}
			System.out.println();
		}

	}
}
